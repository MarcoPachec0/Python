alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
end_project = False
while not end_project:
    print(logo)
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    shift = shift % 26

    def caesar(plain_text, shift_amount):
        cipher_text = ""
        for char in plain_text:
            if char in alphabet:
                position = alphabet.index(char)
                if direction == "encode":
                    new_position = (position + shift_amount) % 26
                elif direction == "decode":
                    new_position = (position - shift_amount) % 26
                cipher_text += alphabet[new_position]
            else:
                cipher_text += char
        return cipher_text

    result = caesar(plain_text=text, shift_amount=shift)
    if direction == "encode":
        print(f"The encoded text is {result}")
    elif direction == "decode":
        print(f"The decoded text is {result}")

    project = input("Type 'yes' if you want to go again. Otherwise type 'no'.\n")
    if project == "no":
        end_project = True